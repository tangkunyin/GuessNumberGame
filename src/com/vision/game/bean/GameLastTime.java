package com.vision.game.bean;
/**
 * 游戏持续时间
 * @author tangkunyin
 */
public class GameLastTime {
	private String acLastSec;
	private String acLastMin;
	private String acLastHr;
	
	private String openAcLastSec;
	private String openAcLastMin;
	private String openAcLastHr;
	public String getAcLastSec() {
		return acLastSec;
	}
	public void setAcLastSec(String acLastSec) {
		this.acLastSec = acLastSec;
	}
	public String getAcLastMin() {
		return acLastMin;
	}
	public void setAcLastMin(String acLastMin) {
		this.acLastMin = acLastMin;
	}
	public String getAcLastHr() {
		return acLastHr;
	}
	public void setAcLastHr(String acLastHr) {
		this.acLastHr = acLastHr;
	}
	public String getOpenAcLastSec() {
		return openAcLastSec;
	}
	public void setOpenAcLastSec(String openAcLastSec) {
		this.openAcLastSec = openAcLastSec;
	}
	public String getOpenAcLastMin() {
		return openAcLastMin;
	}
	public void setOpenAcLastMin(String openAcLastMin) {
		this.openAcLastMin = openAcLastMin;
	}
	public String getOpenAcLastHr() {
		return openAcLastHr;
	}
	public void setOpenAcLastHr(String openAcLastHr) {
		this.openAcLastHr = openAcLastHr;
	}
}
